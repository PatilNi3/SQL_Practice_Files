-- # What is SQL ?

-- SQL (Structured Query Language) is used to perform operations on the records stored in the database, such as updating records, creating and modifying database tables, views etc.
-- SQL is the standard language for Relational DataBase Management System.
-- All the Relational DataBase Management System (RDBMS) use SQL as their standard databse language.

-- # Why it is used for ?

-- The basic use of SQL for data professionals and sql users is to create, insert update and delete the data from the relational databse.
-- SQL allows the data profesionals and users to retrieve the data from the relational database management system.
-- It allows user to create, drop and manipulate the database and its tables.
-- It allows you to define the data and modify that stored data in the relational database.
-- and many more

-- # What is DataBase ?

-- Data means meaningful information, wheather it is structured or unstructured.
-- a database is an organised collection of structuralinformation or data. Typically stored electronically on computer system.
-- To interact with the database we need a language, this language is known as SQL.

-- # Difference between  DBMS and RDBMS

-- ♦ DBMS
-- DBMS stands for DataBase Management System.
-- DBMS stores data as file.
-- No relationship between data.
-- It stores a data in either navigatinal or hierarchical form.
-- It deals with small quantity of data.
-- It supports single users.
-- Example: xml, Windows registry etc.

-- ♦ RDBMS
-- RDBMS stands for Relational DataBase Management System.
-- RDBMS stores data in tabular form.
-- Data is stored in the form of tables whch are related to each other.
-- It uses a tabular structure where the headers are the column names and the row contains corresponding values.
-- It deals with large amount of data.
-- It suppors multiple users.
-- Example: My SQL, SQL Server, Oracle, PostgreSQL.

-- # DDL (Data Definition Language)
-- Structure related command - add column, delete column, alter/change datatype, rename column name, nothing to do with data.
-- It is used to create, modify and delete database structures but not data.
-- We can;t use where clause in DDL command.
-- Create: The command is used to create database or its objects (like table, index, funcrion etc.)
-- Alter: This is used to alter the structure of the table.
-- Truncate: This is used to remove all records from a table.
-- Commnet: This is used to add comments to the dictionary.
-- Rename: This is used to rename an object existing in the database.

-- # DML (Data Manipulation Language)
-- Data related command
-- It is used to retreive and manipulate data in a relational database.
-- Insert: It is used to insert data into table.
-- Update: It is used to update existing data within a table.
-- Delete: It is used to delete records from a database table.





